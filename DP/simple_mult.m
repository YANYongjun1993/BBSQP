function f = simple_mult(x)
% global theta
theta = 0;
Cd       = 0.373;%air drag resistance coefficient
Af       = 2.58;%frontal area
Rho      = 1.205;%density of ambient air
M        = 1870;%vehicle mass
f        = 0.011;%rolling friction coefficient
g        = 9.8;%gravitational acceleration
Eta      = 0.94;%powertrain efficiency
r        = 0.364;%tire radius
v_ref    = 20;%velocity set [m/s]
Ig       = 2.63;%6th gear ratio include final gear
kk        = 30*Ig/(3.14*r);%simplify
p1       = 0.00385165576001348;
p2       = 1.19220483871151E-6;
p3       = 2.95706278924791E-10;
p4       = 5.45299140436618E-5;
p5       = -1.59515599116976E-9;
p6       = 1.74678055479864E-7;
T        = (r*(Cd*Af*Rho*x^2*0.5 + M*g*(f*cos(theta) + sin(theta)))/(Eta*Ig));
dis      = 1;
% Tmax     = (r*(Cd*Af*Rho*20^2*0.5 + M*g*(f*cos(theta) + sin(theta)))/(Eta*Ig));
% fmax     = p1*k + p2*k^2*20 + p3*k^3*20^2 + p4*k*Tmax + p5*k^2*20*Tmax + p6*k*Tmax.^2;
% Tmin     = (r*(Cd*Af*Rho*10^2*0.5 + M*g*(f*cos(theta) + sin(theta)))/(Eta*Ig));
% fmin     = p1*k + p2*k^2*10 + p3*k^3*10^2 + p4*k*Tmin + p5*k^2*10*Tmin + p6*k*Tmin.^2;
f(:,1)   = (p1*kk + p2*kk^2*x + p3*kk^3*x^2 + p4*kk*T + p5*kk^2*x*T + p6*kk*T.^2);
f(:,2)   = dis/x;