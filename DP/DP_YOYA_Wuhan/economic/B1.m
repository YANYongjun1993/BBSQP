%B.m
%刹车
function b=B1(v0,v1,a)
v0=v0/3.6;
v1=v1/3.6;

%% model parameter
cd       = 0.373;%风阻系数
Af       = 2.58;%有效迎风面积
ro       = 1.205;%空气密度
M        = 1870;%整车质量
f        = 0.011;%滚动阻力系数
g        = 9.8;%重力加速度
itat     = 0.94;%机械效率
rw       = 0.364;%车轮半径
Ig       = 2.757;%6挡位传动比：0.672*主减速比：4.103
h =10;

c1=(v1-v0)/h;
c2=cd*Af*ro*v0/(2*M);
c3=g*(f+sin(a))/v0;
b=-(rw*M*v0)*(c1+c2+c3);
end